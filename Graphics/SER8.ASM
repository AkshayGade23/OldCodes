DATA SEGMENT
     N DB 10H,20H,30H,40H,50H
     COUNT EQU O5H
     SUM DW ?   
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
      MOV DS,AX
      MOV CX,COUNT
      MOV AX,00H
      MOV BX,00H
      MOV SI,OFFSET N
AGAIN:MOV BL,[SI]
      ADD AX,BX
      INC SI
      DEC CX
      JNZ AGAIN
      MOV DI,OFFSET SUM
      MOV [DI],AX
CODE ENDS
END START
